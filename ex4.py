import numpy as np


def construct_G_matrix(A,q):
    N = A.shape[0]

    G = np.zeros(shape=(N,N),dtype=np.double)

    q_n = q/N
    q_probability = 1-q

    for i in range(N):
        for j in range(N):
            nj = sum(A[j][n] for n in range(N))
            G[i][j] = q_n + ((A[j][i]*q_probability)/nj)

    return G

def check_G_matrix(G):
    N = G.shape[0]

    for i in range(N):
        row_sum = sum(G[n][i] for n in range(N))
        if row_sum != 1:
            return False
    
    return True


def power_method(G, iterations: int):
    
    N = G.shape[0]
    to_return = np.random.rand(N)

    for i in range(iterations):
        new_mat = np.dot(G, to_return)

        # calculate norm, so we don't search 
        # for not == 0 element
        norm = np.linalg.norm(new_mat)

        to_return = new_mat / norm

    return to_return 
   

